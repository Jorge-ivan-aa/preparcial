package EjerciciosOscar.Recursividad.Clase.Model;

public class Estudiante {
    private int edad;
    private String nombre;

    public Estudiante(){ }

    public int getEdad() {
        return edad;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
}